#include "../../../includes/shared.h"

void	ft_free_map(t_map *m)
{
	int	c;

	c = -1;
	if (m->f)
	{
		while (++c < m->column)
		{
			free(m->f[c]);
			m->f[c] = 0;
		}
		free(m->f);
		m->f = 0;
	}
}
