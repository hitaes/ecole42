#include "../push_swap.h"

int		validator_num(char *s)
{
	int		i;

	i = 0;
	if (!(s[i]))
		return (0);
	while (s[i])
	{
		if (!(ft_isdigit(s[i])) && s[0] != '-' && s[0] != '+')
			return (0);
		i++;
	}
	return (validator_int(s));
}
